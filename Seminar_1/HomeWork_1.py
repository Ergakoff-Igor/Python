# Задача 2
# Найдите сумму цифр трехзначного числа.
# Пример:
# 123 -> 6 (1 + 2 + 3)
# 100 -> 1 (1 + 0 + 0)

# # Метод 1:
def GetSum1(num):
    sum = 0
    i = 0
    while num > 0:
        i = num % 10
        sum += i
        num //= 10
    return sum

# # Метод 2:
def GetSum2(number):
    num = str(number)
    sum = 0
    i = 0
    while i < len(num):
        sum += int(num[i])
        i += 1
    return sum

print('*** Задача № 2 ***')
Number = int(input("Введите трехзначное число: "))
if 99 < int (Number) < 1000:
    print (f'Метод 1: Сумма цифр числа {Number} равна {GetSum1(Number)}')
    print (f'Метод 2: Сумма цифр числа {Number} равна {GetSum2(Number)}')
else:
    print (f'Число {Number} не является трехзначным')


