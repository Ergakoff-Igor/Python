# Пример:
# 3 2 4 -> yes
# 3 2 1 -> no

def Chocolate(n, m, k):
    if (k == 0):
        print('Жаль, что Вы не хотите шоколадку. Она очень вкусная!')
        return
    elif (k == n * m):
        print(f'Если вам нужно {k} кусочков то можете забрать шоколадку размером {n}x{m} целиком')
        return
    elif (k % n == 0 or k % m == 0):
        print(f'От шоколадки размером {n}x{m} можно отломить {k} кусочков за 1 раз')
        return
    else:
        print(f'От шоколадки размером {n}x{m} нельзя отломить {k} кусочков за 1 раз')
        return

