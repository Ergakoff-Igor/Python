# Задача 10
# На столе лежат n монеток. Некоторые из них лежат вверх решкой, а некоторые – гербом.
# Определите минимальное число монеток, которые нужно перевернуть, чтобы все монетки были повернуты вверх одной и той же стороной.
# Выведите минимальное количество монет, которые нужно перевернуть.
# 5 -> 1 0 1 1 0
# 2
print ('Решение задачи № 10')
while True:
    try:
        N = int(input("Введите количество монеток: "))
        if N > 1:
            import random
            cash = []
            for _ in range(N):
                cash.append(random.randint(0, 1))
            print(cash)
            print()

            count_0 = 0
            count_1 = 0
            for item in cash:
                if item == 0:
                    count_0 += 1
                elif item == 1:
                    count_1 += 1

            if count_0 == 0 or count_1 == 0:
                print(f' Все {N} монет лежат орлом или решкой вверх')
                break
            else:
                if count_0 >= count_1:
                    print(f' Из {N} монет нужно перевернуть минимум {count_1} шт.')
                elif count_0 < count_1:
                    print(f' Из {N} монет нужно перевернуть минимум {count_0} шт.')
                break
        elif N == 0:
            print('Вы ввели 0, такого количества монет не может быть')
            break
        elif N == 1:
            print('Вы ввели одну монету, её не нужно переворачивать')
            break
    except:
        print('Некорректный ввод. Попробуйте еще раз!')

