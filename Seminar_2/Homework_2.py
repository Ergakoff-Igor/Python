# Помогите Кате отгадать задуманные Петей числа.

print ('Решение задачи № 12')
while True:
    try:
        S = int(input("Введите сумму двух чисел: "))
        P = int(input("Введите произведение двух чисел: "))
                        
        for num1 in range(0, 1000):
            for num2 in range(0, 1000):
                if (num1 + num2 == S) and (num1 * num2 == P):
                    print(f'Первое число -> {num1}, второе число -> {num2}')
                    break
        break
    except:
        print('Некорректный ввод. Попробуйте еще раз!')
        
